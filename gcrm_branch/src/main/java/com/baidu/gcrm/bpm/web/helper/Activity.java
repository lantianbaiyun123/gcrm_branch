package com.baidu.gcrm.bpm.web.helper;

import java.util.Date;

import com.baidu.gcrm.bpm.model.ProcessActivityType.ActivitySubtype;
import com.baidu.gcrm.bpm.model.ProcessActivityType.ActivityType;

public class Activity {
	private String processDefId;
	
	private String processId;
	
	private String processName;
	
	private String startUser;
	
	private Date startTime;
	
	private String activityId;
	
	private String actDefId;
	
	private String activityName;
	
	private Date activityArrivedTime;
	
	private String performer;
	
	private String foreignName;
	
	private String foreignKey;
	
	private String number;
	
	private ActivityType type = ActivityType.approval;
	
	private ActivitySubtype subtype = ActivitySubtype.schedule;

	public String getProcessDefId() {
		return processDefId;
	}

	public void setProcessDefId(String processDefId) {
		this.processDefId = processDefId;
	}

	public String getProcessId() {
		return processId;
	}

	public void setProcessId(String processId) {
		this.processId = processId;
	}

	public String getProcessName() {
		return processName;
	}

	public void setProcessName(String processName) {
		this.processName = processName;
	}

	public String getStartUser() {
		return startUser;
	}

	public void setStartUser(String startUser) {
		this.startUser = startUser;
	}

	public Date getStartTime() {
		return startTime;
	}

	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}

	public String getActivityId() {
		return activityId;
	}

	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}

	public String getActDefId() {
		return actDefId;
	}

	public void setActDefId(String actDefId) {
		this.actDefId = actDefId;
	}

	public String getActivityName() {
		return activityName;
	}

	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}

	public Date getActivityArrivedTime() {
		return activityArrivedTime;
	}

	public void setActivityArrivedTime(Date activityArrivedTime) {
		this.activityArrivedTime = activityArrivedTime;
	}

	public String getPerformer() {
		return performer;
	}

	public void setPerformer(String performer) {
		this.performer = performer;
	}

	public String getForeignName() {
		return foreignName;
	}

	public void setForeignName(String foreignName) {
		this.foreignName = foreignName;
	}

	public String getForeignKey() {
		return foreignKey;
	}

	public void setForeignKey(String foreignKey) {
		this.foreignKey = foreignKey;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public ActivityType getType() {
		return type;
	}

	public void setType(ActivityType type) {
		this.type = type;
	}

	public ActivitySubtype getSubtype() {
		return subtype;
	}

	public void setSubtype(ActivitySubtype subtype) {
		this.subtype = subtype;
	}

	@Override
	public String toString() {
		return "Activity [processDefId=" + processDefId + ", startUser=" + startUser + ", startTime=" + startTime
				+ ", activityId=" + activityId + ", foreignKey=" + foreignKey + ", subtype=" + subtype + "]";
	}
	

}
