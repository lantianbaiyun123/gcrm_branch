package com.baidu.gcrm.bpm.web.helper;

import com.baidu.gcrm.bpm.vo.ParticipantConstants;

public class ActivityInfo {
	private ParticipantConstants participantId;
	
	private boolean isRunning;
	
	private String performer;
	private String foreignKey;

	public ParticipantConstants getParticipantId() {
		return participantId;
	}

	public void setParticipantId(ParticipantConstants participantId) {
		this.participantId = participantId;
	}

	public boolean isRunning() {
		return isRunning;
	}

	public void setRunning(boolean isRunning) {
		this.isRunning = isRunning;
	}

	public String getPerformer() {
		return performer;
	}

	public void setPerformer(String performer) {
		this.performer = performer;
	}

	public String getForeignKey() {
		return foreignKey;
	}

	public void setForeignKey(String foreignKey) {
		this.foreignKey = foreignKey;
	}

}
