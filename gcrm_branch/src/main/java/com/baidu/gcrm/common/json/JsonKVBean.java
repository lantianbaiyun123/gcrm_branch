package com.baidu.gcrm.common.json;

public class JsonKVBean {
    
    String key;
    String value;
    Object[] args;
    
    public JsonKVBean(String key, String value, Object[] args) {
        this.key = key;
        this.value = value;
        this.args = args;
    }
    
    public String getKey() {
        return key;
    }
    public void setKey(String key) {
        this.key = key;
    }
    public String getValue() {
        return value;
    }
    public void setValue(String value) {
        this.value = value;
    }
    public Object[] getArgs() {
        return args;
    }
    public void setArgs(Object[] args) {
        this.args = args;
    }

}
