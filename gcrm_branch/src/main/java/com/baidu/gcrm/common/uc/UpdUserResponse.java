package com.baidu.gcrm.common.uc;

public class UpdUserResponse {
    
    private Long err_no;
    
    private String msg;

    public Long getErr_no() {
		return err_no;
	}

	public void setErr_no(Long err_no) {
		this.err_no = err_no;
	}

	public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

	@Override
	public String toString() {
		return "UpdUserResponse [err_no=" + err_no + ", msg=" + msg + "]";
	}
}
