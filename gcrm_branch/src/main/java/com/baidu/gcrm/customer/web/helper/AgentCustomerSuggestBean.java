package com.baidu.gcrm.customer.web.helper;

import com.baidu.gcrm.common.AutoSuggestBean;

public class AgentCustomerSuggestBean extends AutoSuggestBean {

    public AgentCustomerSuggestBean(String id, String tag, String name) {
        super(id, tag, name);
    }
    public AgentCustomerSuggestBean(String id, String tag, String name,Integer companyStatus) {
        super(id, tag, name);
        setCompanyStatus(companyStatus);
    }
    private Integer companyStatus;
    
    public Integer getCompanyStatus() {
        return companyStatus;
    }
    public void setCompanyStatus(Integer companyStatus) {
        this.companyStatus = companyStatus;
    }



 
}
