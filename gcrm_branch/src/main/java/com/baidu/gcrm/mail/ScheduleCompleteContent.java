package com.baidu.gcrm.mail;

import java.util.Set;

import com.baidu.gcrm.common.LocaleConstants;

public class ScheduleCompleteContent {
	/**	收件人 */
	private Set<String> sendTo;
	
	/**	抄送 */
	private Set<String> cc;
	
	/**	广告方案提交时间*/
	private String adSubmitTime;
	
	/**	国代提交的投放时间 */
	private String occupationPeriods;
	
	/**	国代提交的插单时间 */
	private String insertPeriods;
	
	/**	广告内容编号 */
	private String adContentNumber;
	
	/**	国代操作人 */
	private String countryAgent;
	
	private LocaleConstants locale;

	/**
	 * 原投放时间
	 */
	private String oldOccupationPeriods;
	
	private String operator;
	
	private Long adSolutionId;
	
	public Set<String> getSendTo() {
		return sendTo;
	}

	public void setSendTo(Set<String> sendTo) {
		this.sendTo = sendTo;
	}

	public String getAdSubmitTime() {
		return adSubmitTime;
	}

	public void setAdSubmitTime(String adSubmitTime) {
		this.adSubmitTime = adSubmitTime;
	}

	public String getOccupationPeriods() {
		return occupationPeriods;
	}

	public void setOccupationPeriods(String occupationPeriods) {
		this.occupationPeriods = occupationPeriods;
	}

	public String getInsertPeriods() {
		return insertPeriods;
	}

	public void setInsertPeriods(String insertPeriods) {
		this.insertPeriods = insertPeriods;
	}

	public String getAdContentNumber() {
		return adContentNumber;
	}

	public void setAdContentNumber(String adContentNumber) {
		this.adContentNumber = adContentNumber;
	}

	public String getCountryAgent() {
		return countryAgent;
	}

	public void setCountryAgent(String countryAgent) {
		this.countryAgent = countryAgent;
	}

	public LocaleConstants getLocale() {
		return locale;
	}

	public void setLocale(LocaleConstants locale) {
		this.locale = locale;
	}

	public String getOldOccupationPeriods() {
		return oldOccupationPeriods;
	}

	public void setOldOccupationPeriods(String oldOccupationPeriods) {
		this.oldOccupationPeriods = oldOccupationPeriods;
	}

	public Set<String> getCc() {
		return cc;
	}

	public void setCc(Set<String> cc) {
		this.cc = cc;
	}

	public String getOperator() {
		return operator;
	}

	public void setOperator(String operator) {
		this.operator = operator;
	}

	public Long getAdSolutionId() {
		return adSolutionId;
	}

	public void setAdSolutionId(Long adSolutionId) {
		this.adSolutionId = adSolutionId;
	}
	
}
