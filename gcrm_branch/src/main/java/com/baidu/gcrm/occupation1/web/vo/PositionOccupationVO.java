package com.baidu.gcrm.occupation1.web.vo;

import java.util.Date;
import java.util.List;

/**
 * 指定位置在时间范围内每天的库存占用情况
 * @author anhuan
 *
 */
public class PositionOccupationVO {

    private Long positionId;
    
    private Long billingModelId;
    
    private Date from;
    
    private Date to;
    
    private boolean rotation;
    
    private int totalCount;
    
    private List<DateOccupation> occupations;

    public Long getPositionId() {
        return positionId;
    }

    public void setPositionId(Long positionId) {
        this.positionId = positionId;
    }

    public Long getBillingModelId() {
        return billingModelId;
    }

    public void setBillingModelId(Long billingModelId) {
        this.billingModelId = billingModelId;
    }

    public Date getFrom() {
        return from;
    }

    public void setFrom(Date from) {
        this.from = from;
    }

    public Date getTo() {
        return to;
    }

    public void setTo(Date to) {
        this.to = to;
    }

    public boolean isRotation() {
        return rotation;
    }

    public void setRotation(boolean rotation) {
        this.rotation = rotation;
    }

    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public List<DateOccupation> getOccupations() {
        return occupations;
    }

    public void setOccupations(List<DateOccupation> occupations) {
        this.occupations = occupations;
    }
    
}
