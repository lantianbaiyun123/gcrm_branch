package com.baidu.gcrm.quote.web.vo;

import java.util.List;

public class QuotationMainConflictMsg {
	private String platformName;
	private String quoteCode;
	private String endTime;
	private String startTime;
	private String siteName;
	private List<String> msgPeriods;
	
	public String getPlatformName() {
		return platformName;
	}
	public void setPlatformName(String platformName) {
		this.platformName = platformName;
	}
	public String getQuoteCode() {
		return quoteCode;
	}
	public void setQuoteCode(String quoteCode) {
		this.quoteCode = quoteCode;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getSiteName() {
		return siteName;
	}
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
	public List<String> getMsgPeriods() {
		return msgPeriods;
	}
	public void setMsgPeriods(List<String> msgPeriods) {
		this.msgPeriods = msgPeriods;
	}
}
