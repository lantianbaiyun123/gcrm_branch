package com.baidu.gcrm.resource.position.web.vo;

import com.baidu.gcrm.common.page.Page;

public class PositionCondition<T> extends Page<T>{
    
    private static final long serialVersionUID = -3025034093179659511L;

    private String positionName;
    
    private String positionNumber;
    
    private String areaRequired;;
    
    private Long adPlatformId;
    
    private Long siteId;
    
    private Long channelId;
    
    private Long areaId;
    
    private Long positionId;
    
    private Integer type;

    public String getPositionName() {
        return positionName;
    }

    public void setPositionName(String positionName) {
        this.positionName = positionName;
    }

    public Long getAdPlatformId() {
        return adPlatformId;
    }

    public void setAdPlatformId(Long adPlatformId) {
        this.adPlatformId = adPlatformId;
    }

    public Long getSiteId() {
        return siteId;
    }

    public void setSiteId(Long siteId) {
        this.siteId = siteId;
    }

    public Long getChannelId() {
        return channelId;
    }

    public void setChannelId(Long channelId) {
        this.channelId = channelId;
    }

    public Long getAreaId() {
        return areaId;
    }

    public void setAreaId(Long areaId) {
        this.areaId = areaId;
    }

    public Long getPositionId() {
        return positionId;
    }

    public void setPositionId(Long positionId) {
        this.positionId = positionId;
    }

    public String getAreaRequired() {
        return areaRequired;
    }

    public void setAreaRequired(String areaRequired) {
        this.areaRequired = areaRequired;
    }

    public String getPositionNumber() {
        return positionNumber;
    }

    public void setPositionNumber(String positionNumber) {
        this.positionNumber = positionNumber;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }
    

}
