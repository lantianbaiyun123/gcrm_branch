package com.baidu.gcrm.schedule.web.vo;

import java.util.Date;
import java.util.List;

public class ScheduleQueryAndSaveVO {
    
    private Long adContentId;
    private Long positionId;
    private Date startDate;
    private int days;
    private List<ScheduleInfoVO> scheduleDates;
    private List<ConflictInfoVo> conflictInfos;
    private Date endDate;
    private String activityId;
    private String processId;
    
    public Long getAdContentId() {
        return adContentId;
    }
    public void setAdContentId(Long adContentId) {
        this.adContentId = adContentId;
    }
    public Long getPositionId() {
        return positionId;
    }
    public void setPositionId(Long positionId) {
        this.positionId = positionId;
    }
    public Date getStartDate() {
        return startDate;
    }
    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }
    public int getDays() {
        return days;
    }
    public void setDays(int days) {
        this.days = days;
    }
    public List<ScheduleInfoVO> getScheduleDates() {
        return scheduleDates;
    }
    public void setScheduleDates(List<ScheduleInfoVO> scheduleDates) {
        this.scheduleDates = scheduleDates;
    }
	public List<ConflictInfoVo> getConflictInfos() {
		return conflictInfos;
	}
	public void setConflictInfos(List<ConflictInfoVo> conflictInfos) {
		this.conflictInfos = conflictInfos;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public String getActivityId() {
		return activityId;
	}
	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}
	public String getProcessId() {
		return processId;
	}
	public void setProcessId(String processId) {
		this.processId = processId;
	}
}
