package com.baidu.gcrm.user.web.vo;

/**
 * 用户详细信息VO
 * 
 * @author zhanglei35
 * 
 */
public class UserDetailInfoVO {
	private UserVO userVO;
	private String sites;
	private String businessTypes;
	private String platforms;
	private String roleName;
	private String posParent;
	private String posSub;
	private String position;
	private boolean hasDataRights;
	private String newRightsURL;

	public UserVO getUserVO() {
		return userVO;
	}

	public void setUserVO(UserVO userVO) {
		this.userVO = userVO;
	}

	public String getSites() {
		return sites;
	}

	public void setSites(String sites) {
		this.sites = sites;
	}

	public String getBusinessTypes() {
		return businessTypes;
	}

	public void setBusinessTypes(String businessTypes) {
		this.businessTypes = businessTypes;
	}

	public String getPlatforms() {
		return platforms;
	}

	public void setPlatforms(String platforms) {
		this.platforms = platforms;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getPosParent() {
		return posParent;
	}

	public void setPosParent(String posParent) {
		this.posParent = posParent;
	}

	public String getPosSub() {
		return posSub;
	}

	public void setPosSub(String posSub) {
		this.posSub = posSub;
	}

	public boolean isHasDataRights() {
		return hasDataRights;
	}

	public void setHasDataRights(boolean hasDataRights) {
		this.hasDataRights = hasDataRights;
	}

	public String getNewRightsURL() {
		return newRightsURL;
	}

	public void setNewRightsURL(String newRightsURL) {
		this.newRightsURL = newRightsURL;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

}
