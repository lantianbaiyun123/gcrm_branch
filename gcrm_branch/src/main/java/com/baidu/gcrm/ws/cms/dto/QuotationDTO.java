package com.baidu.gcrm.ws.cms.dto;

import java.io.Serializable;
import java.util.Date;

/**
 * 价格政策DTO
 *
 */
public class QuotationDTO implements Serializable{
    
    private static final long serialVersionUID = 6212561458121326655L;

	private Long id; //主键
	
	private Long productId;//产品id
	
	private Long siteId;//站点id
	
	private Long billingModelId;//计费方式ID
	
	private Double ratioMine;//我方分成比例
	
	private Double ratioCustomer;//客户方分成比例
	
	private Double ratioRebate;//返点比例
	
	private Double ratioThird;//第三方分成
	
	private Double publishPrice;//发行价
	
	private Date startTime;//开始日期
	
	private Date endTime;//结束日期
	
	private Integer priceType;//价格类型，0广告单价，1分成，2返点
	
	private Integer industryType;//行业类型，0电商，1游戏，2其他，3ALL
	
	private Integer businessType;//业务类型，0 变现  1 销售
	
	private Integer status;//状态，0无效，1有效，2超期失效，3作废
	
	
    public Long getId() {
	    return id;
    }

    public void setId(Long id) {
	   this.id = id;
    }

	public Long getProductId() {
		return productId;
	}

	public void setProductId(Long productId) {
		this.productId = productId;
	}

	public Long getSiteId() {
		return siteId;
	}

	public void setSiteId(Long siteId) {
		this.siteId = siteId;
	}

	public Long getBillingModelId() {
		return billingModelId;
	}

	public void setBillingModelId(Long billingModelId) {
		this.billingModelId = billingModelId;
	}

	public Double getRatioMine() {
		return ratioMine;
	}

	public void setRatioMine(Double ratioMine) {
		this.ratioMine = ratioMine;
	}

	public Double getRatioThird() {
		return ratioThird;
	}

	public void setRatioThird(Double ratioThird) {
		this.ratioThird = ratioThird;
	}

	public Double getPublishPrice() {
		return publishPrice;
	}

	public void setPublishPrice(Double publishPrice) {
		this.publishPrice = publishPrice;
	}

    public Double getRatioCustomer() {
        return ratioCustomer;
    }

    public void setRatioCustomer(Double ratioCustomer) {
        this.ratioCustomer = ratioCustomer;
    }

    public Double getRatioRebate() {
        return ratioRebate;
    }

    public void setRatioRebate(Double ratioRebate) {
        this.ratioRebate = ratioRebate;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public Integer getPriceType() {
        return priceType;
    }

    public void setPriceType(Integer priceType) {
        this.priceType = priceType;
    }

    public Integer getIndustryType() {
        return industryType;
    }

    public void setIndustryType(Integer industryType) {
        this.industryType = industryType;
    }

    public Integer getBusinessType() {
        return businessType;
    }

    public void setBusinessType(Integer businessType) {
        this.businessType = businessType;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

	
}
